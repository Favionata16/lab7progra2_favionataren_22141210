        for (Equipo equi : equipos) {
            if(equi.getNombre().equalsIgnoreCase(nomequi.getText())){
                x=true;
                break;
            }
        }
            if (x==false) {
                file = new File("./equipos.txt");
            equipos.add(new Equipo(nomequi.getText(),0,0,0,0,0,0,0,0));
            try {
                escribirArchivo();
            } catch (IOException ex) {
                
            }
            }else{
                JOptionPane.showMessageDialog(this, "se estan creando dos equipos iguales!");
            }
